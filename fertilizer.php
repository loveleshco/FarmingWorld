        <p>Fertilizers are used daily by farmers and families to help crops and gardens grow. 
            Whether for a small garden of flowers and plants, or a large farm with thousands 
            of acres of crops, a wide range of fertilizers have been developed to help different crops grow in different soil and weather conditions.
            
            Chemical ingredients help create fertilizers that promote plant growth and are cost effective, too.
             Commercial and consumer fertilizers are strictly regulated by both individual states and the federal government to ensure that they are safe for 
             the people who use them, people nearby, and the surrounding environment.</p>
             <h2>Uses and benefits</h2>
             <p>With the global population steadily growing, it is important that enough crops are produced each year to provide food,
                  clothing and other agricultural products to people around the world. Crops such as corn, wheat and cotton receive nutrients from the soil they are
                   grown in; various crops deplete soil nutrients in different ways and rates. Some crop growth can deplete soil nutrients after just a few seasons 
                   of planting. Fertilizers play an important role in providing crops with the nutrients they need to grow and be harvested for nutritious food.
                    
                    Fertilizers help deliver enough food to feed the world’s population. But they can do even more. 
                    A class of fertilizers called micronutrient fertilizers is engineered to enrich crops with vital nutrients that help support human health.
                     For example, micronutrients such as zinc are important to human nutrition, especially children. According to a United Nations study, much of the 
                     world’s grain crops are grown in soil without adequate zinc; offering micronutrient fertilizers to grain crops enriches the grain with an important 
                     nutrient.
                    
                   </p>
                   <h2>Safety Information</h2>
                   <p>
                        Fertilizers are essential to the security of the world’s food supply, and they must be used properly. 
                        The manufacture, sale and transportation of fertilizers is heavily regulated. States have difference regulations and statutes that address 
                        fertilizer use and production to protect human health and the environment.
                        
                        In addition to individual state regulations, the federal government also regulates fertilizer production, use and transport. 
                        Fertilizer producers are required to participate in the U.S. Occupational Safety and Health Administration’s (OSHA) Process Safety 
                        Management Regulation to protect worker safety. In addition to OSHA, the U.S. Department of Agriculture, Environmental Protection Agency, 
                        Department of Homeland Security and Department of Transportation all play roles in regulating fertilizers and their production.
                        
                        Ammonium nitrate, a good source of nitrogen and ammonium for plants, is an important ingredient in the production of high quality,
                         effective fertilizers. Ammonium nitrate is produced using anhydrous ammonia, a gas that can become explosive when mixed with air. 
                         While this fertilizer is applied safely every day, this feature also means that it can be dangerous in the wrong hands. 
                         Fertilizer producers take great care in the safe use and storage of anhydrous ammonia and have implemented safety measures to help 
                         prevent or mitigate an incident.
                   </p>
